package com.example.voaenglish.utils;

public class IntentServiceResult {

    int mResult;
    String mResultValue;

    public IntentServiceResult(int mResult, String mResultValue) {
        this.mResult = mResult;
        this.mResultValue = mResultValue;
    }

    public int getmResult() {
        return mResult;
    }

    public String getmResultValue() {
        return mResultValue;
    }
}
